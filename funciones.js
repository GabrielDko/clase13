let suma = function(numeroA,numeroB){
    return numeroA + numeroB
}
let resta = function(numeroA,numeroB){
    return numeroA - numeroB
}
let multiplicacion = function(numeroA,numeroB){
    return numeroA * numeroB
}
let division = function(numeroA,numeroB){
    return numeroA / numeroB
}
let resto = function(numeroA,numeroB){
    return numeroA % numeroB
}